package br.com.placas.beans;

public class Placas {
    private int id;
    private String data_encomenda;
    private String cor_placa;
    private String cor_frase;
    private String frase;
    private double altura;
    private double largura;
    private double valor_placa;
    private double valor_entrada;
    private double valor_total;
    private String data_entrega;
    private String status;
    private String cliente;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getData_encomenda() {
        return data_encomenda;
    }

    public void setData_encomenda(String data_encomenda) {
        this.data_encomenda = data_encomenda;
    }
    
    

    public String getCor_placa() {
        return cor_placa;
    }

    public void setCor_placa(String cor_placa) {
        this.cor_placa = cor_placa;
    }

    public String getCor_frase() {
        return cor_frase;
    }

    public void setCor_frase(String cor_frase) {
        this.cor_frase = cor_frase;
    }

    public String getFrase() {
        return frase;
    }

    public void setFrase(String frase) {
        this.frase = frase;
    }

    public double getAltura() {
        return altura;
    }

    public void setAltura(double altura) {
        this.altura = altura;
    }

    public double getLargura() {
        return largura;
    }

    public void setLargura(double largura) {
        this.largura = largura;
    }

    public double getValor_placa() {
        return valor_placa;
    }

    public void setValor_placa(double valor_placa) {
        this.valor_placa = valor_placa;
    }
    
    
    
    public double getValor_entrada() {
        return valor_entrada;
    }

    public void setValor_entrada(double valor_entrada) {
        this.valor_entrada = valor_entrada;
    }

    public double getValor_total() {
        return valor_total;
    }

    public void setValor_total(double valor_total) {
        this.valor_total = valor_total;
    }

    public String getData_entrega() {
        return data_entrega;
    }

    public void setData_entrega(String data_entrega) {
        this.data_entrega = data_entrega;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getCliente() {
        return cliente;
    }

    public void setCliente(String cliente) {
        this.cliente = cliente;
    }
    
    
    
}
